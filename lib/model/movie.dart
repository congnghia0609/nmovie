class Movie {
  final int id;
  final double popularity;
  final String title;
  final String backPoster;
  final String poster;
  final String overview;
  final double rating;

  Movie(this.id, this.popularity, this.title, this.backPoster, this.poster, this.overview, this.rating);

  Movie.fromJson(Map<String, dynamic> json)
      : id = json["id"],
        popularity = json["popularity"],
        title = json["title"],
        backPoster = json["backdrop_path"],
        poster = json["poster_path"],
        overview = json["overview"],
        rating = json["vote_average"].toDouble();

  @override
  String toString() {
    return 'Movie{id: $id, popularity: $popularity, title: $title, backPoster: $backPoster, poster: $poster, overview: $overview, rating: $rating}';
  }
}
